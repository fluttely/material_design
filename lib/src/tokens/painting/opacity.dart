/// Opacity values used for interactive states.
abstract final class MaterialOpacity {
  /// The opacity for disabled elements.
  static const double disabled = 0.38;

  /// The opacity for hovered elements.
  static const double hover = 0.08;

  /// The opacity for focused elements.
  static const double focus = 0.12;

  /// The opacity for pressed elements.
  static const double pressed = 0.16;

  /// The opacity for backdrops.
  static const double backdrop = 0.5;

  /// The opacity for dividers.
  static const double divider = 0.12;
}
