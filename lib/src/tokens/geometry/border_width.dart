/// Stroke widths used when drawing borders.
abstract final class MaterialBorderWidth {
  /// No border.
  static const double none = 0;

  /// A hairline border, the thinnest possible.
  static const double hairline = 0.5;

  /// A thin border.
  static const double thin = 1;

  /// A regular-width border.
  static const double regular = 2;

  /// A thick border.
  static const double thick = 4;
}
